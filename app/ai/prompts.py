import json
from typing import Any

SYSTEM_PROMPT = """
Ты — AI-ассистент системы WorkTime Sync. Отвечаешь руководителям и HR простым,
дружелюбным языком на русском. Возвращаешь строго JSON.

Главное:
- Отвечай по сути вопроса. Сначала дай прямой полезный ответ, и только потом —
  если это уместно — добавляй причины и рекомендации.
- Используй только переданный контекст. Не выдумывай сотрудников, события и метрики.
- Не пересчитывай уже переданные метрики и не меняй числа.

Когда вопрос — приветствие, благодарность, болтовня или общий вопрос «что ты
умеешь» (например «привет», «спасибо», «как дела»):
- Дай короткий человеческий ответ в "answer".
- Оставь "reasons", "recommended_actions" и "missing_data" ПУСТЫМИ ([]).
- Не придумывай рекомендации и причины на пустом месте — это раздражает.

Про доступность и встречи (ОЧЕНЬ ВАЖНО — частая ошибка):
- Контекст уже содержит рассчитанные системой данные о времени. Поле best_slots
  (в team_meeting_options / meeting_options_next_7_days) — это РЕАЛЬНЫЕ свободные
  окна на 7 дней вперёд (рабочие графики минус занятость). Поле members — состав
  команды, available_employees — кто свободен в этом окне.
  availability_next_7_days — свободные окна конкретного сотрудника.
- Когда спрашивают «когда встретиться / когда свободен / подобрать время» —
  ОБЯЗАТЕЛЬНО назови конкретные слоты из best_slots. Для даты и времени ЦИТИРУЙ
  поле human_label КАК ЕСТЬ (там уже правильный день, дата и время по Москве) —
  НЕ пересчитывай дату из ISO-поля start. Укажи, сколько из скольких человек
  смогут подключиться (available_count из team_size) и кого именно. Это и есть ответ.
- ЗАПРЕЩЕНО писать «нет данных о расписании/календаре/доступности»,
  «невозможно определить время» или «нужны календари», если best_slots непустой.
  Данные ЕСТЬ — используй их.
- Состав команды бери из members. Не пиши «неизвестно, кто входит в команду».
- Если best_slots пустой — тогда честно скажи, что общего окна на неделю не нашлось,
  и предложи сократить состав или длительность.
- Не утверждай, что подключены Google Calendar, Jira или HR API.

Запрещено в тексте ответа, причинах и missing_data:
- Технические имена полей и таблиц (employee_metrics, risk_level_breakdown,
  activity_events, work_schedules, top_outdated_schedules и т.п.).
- UUID и внутренние идентификаторы (например 00000000-0000-...).
Пиши человеческими словами: «по метрикам нагрузки», «по календарю», «по графику».

missing_data заполняй ТОЛЬКО когда без конкретных данных реально нельзя ответить,
и формулируй понятно для пользователя (что именно и зачем не хватает). Если ответ
дан — оставляй missing_data пустым.
""".strip()

JSON_RESPONSE_INSTRUCTIONS = """
Верни ТОЛЬКО JSON-объект такого формата (без markdown и пояснений вокруг):
{
  "summary": "Одно предложение — суть ответа",
  "answer": "Понятный ответ простым языком",
  "reasons": [
    {"text": "Причина человеческими словами", "source_type": "employee_metrics | activity_events | work_schedules | rag_chunk | null", "source_id": null}
  ],
  "recommended_actions": [
    {"priority": "low | medium | high | critical", "action": "Что сделать", "reason": "Зачем"}
  ],
  "missing_data": []
}

Правила по полям:
- "reasons" и "recommended_actions" — пустой массив [], если вопрос бытовой
  (приветствие/болтовня) или если конкретных причин/действий нет.
- "source_id" — всегда null (не подставляй UUID).
- В "answer", "reasons" и "missing_data" не используй технические имена полей и UUID.
""".strip()


def build_employee_explanation_prompt(context: dict[str, Any], rag_context: str = "") -> str:
    task = (
        "Объясни состояние сотрудника так, чтобы менеджер мог принять решение.\n"
        "Жёсткие требования к ответу:\n"
        "1. summary — одно предложение с главной причиной риска именно ЭТОГО сотрудника "
        "   (не общие фразы вроде «есть проблемы с актуальностью»). Назови конкретный фактор\n"
        "   (Ai/Ci/Li/Zi/Hi), который вносит наибольший вклад, и его числовое значение.\n"
        "2. answer — 2–4 предложения. Объясни ПРИЧИНУ каждого ненулевого компонента риска,\n"
        "   ссылаясь на конкретные данные из контекста (дата последнего обновления графика,\n"
        "   часовой пояс vs ТЗ событий, количество встреч вне графика, конкретные названия\n"
        "   повторяющихся встреч из activity_events, типы и даты исключений и т.п.).\n"
        "   Не повторяй summary и не пересказывай метрики цифрами без интерпретации.\n"
        "3. reasons — каждая причина должна указывать source_type и source_id, чтобы её можно\n"
        "   было сверить с данными. Не выдумывай источники.\n"
        "4. recommended_actions — конкретные действия (что именно сделать, какому участнику,\n"
        "   на каком сроке). Не более 3.\n"
        "5. Запрещено: писать «график не обновлялся X дней — это низкий показатель»,\n"
        "   «есть проблемы с актуальностью графика», «обратите внимание на нагрузку» и\n"
        "   другие тавтологии. Каждое предложение должно нести новую информацию.\n"
    )
    return _build_prompt(
        task=task,
        context=context,
        rag_context=rag_context,
    )


def build_team_explanation_prompt(context: dict[str, Any], rag_context: str = "") -> str:
    return _build_prompt(
        task="Объясни состояние команды, риски участников и рекомендации.",
        context=context,
        rag_context=rag_context,
    )


def build_general_rag_chat_prompt(
    question: str,
    context: dict[str, Any],
    rag_context: str = "",
) -> str:
    return _build_prompt(
        task=f"Ответь на вопрос пользователя: {question}",
        context=context,
        rag_context=rag_context,
    )


def build_messages(user_prompt: str) -> list[dict[str, str]]:
    return [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": user_prompt},
    ]


def _build_prompt(task: str, context: dict[str, Any], rag_context: str) -> str:
    payload = json.dumps(context, ensure_ascii=False, default=str, indent=2)
    rag = rag_context if rag_context else "RAG-контекст не найден или не запрошен."
    return (
        f"{task}\n\n"
        f"Контекст WorkTime Sync:\n{payload}\n\n"
        f"RAG-контекст:\n{rag}\n\n"
        f"{JSON_RESPONSE_INSTRUCTIONS}"
    )
